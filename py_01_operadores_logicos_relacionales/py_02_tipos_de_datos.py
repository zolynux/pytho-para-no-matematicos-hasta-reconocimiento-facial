# Resta
resta = n1 - n2
print(resta)

# Multiplicacion
multiplicacion = n1 * n2
print(multiplicacion)

# Division
division = n1 / n2
print(division)

# Modulo
modulo = n1 % n2
print(modulo)

# Potencia
potencia = n1 ** n2
print(potencia)


